def ip_to_bytes(ip):
    return struct.pack(">BBBB", *map(int,ip.split(".")))

def ip_to_int(ip):
    return struct.unpack(">I", ip_to_bytes(ip))[0]

def parse_query(payload):
    b0, b1, index, val1, val2 = struct.unpack(">BBHII", payload)
    command = b0 >> 4
    address = b1 + ((b0&0xF) << 8)
    return command, address, index, val1, val2

